def test_vacuum_clamped_values(
    s3,
    pg_conn,
    extension,
    grant_access_to_data_file_partition,
    with_default_location,
    col_type,
    col_name,
    partition_by,
    special_val,
    clamped_hi,
):
    """
    Verify that clamped out-of-range values survive VACUUM compaction and
    remain queryable.  The partition key and data must stay consistent
    through the insert → compact → query lifecycle.
    """

    table_name = "test_vacuum_clamped"

    run_command(
        f"""
        CREATE TABLE {table_name} (
            {col_name} {col_type}
        )
        USING pg_lake_iceberg WITH (
            partition_by = '{partition_by}',
            out_of_range_values = 'clamp'
        );
        SET TIME ZONE 'UTC';
        SET pg_lake_table.vacuum_compact_min_input_files TO 1;
    """,
        pg_conn,
    )

    if "numeric" in col_type:
        normal_vals = ["1.5", "2.5"]
    elif col_type == "date":
        normal_vals = ["2024-06-15", "2024-07-20"]
    elif col_type == "timestamp":
        normal_vals = ["2024-06-15 10:00:00", "2024-07-20 12:30:00"]
    else:
        normal_vals = ["2024-06-15 10:00:00+00", "2024-07-20 12:30:00+00"]

    for v in normal_vals:
        run_command(
            f"INSERT INTO {table_name} VALUES ('{v}');",
            pg_conn,
        )
    run_command(
        f"INSERT INTO {table_name} VALUES ('{special_val}');",
        pg_conn,
    )

    total_rows = len(normal_vals) + 1

    # -- pre-compaction checks --
    cnt = run_query(f"SELECT count(*) FROM {table_name};", pg_conn)[0][0]
    assert cnt == total_rows, f"pre-vacuum row count: {cnt}"

    if clamped_hi is not None:
        pre = run_query(
            f"SELECT count(*) FROM {table_name} WHERE {col_name} = '{clamped_hi}';",
            pg_conn,
        )[0][0]
        assert pre == 1, f"pre-vacuum: clamped value not found via {clamped_hi}"
    else:
        pre = run_query(
            f"SELECT count(*) FROM {table_name} WHERE {col_name} IS NULL;",
            pg_conn,
        )[0][0]
        assert pre == 1, "pre-vacuum: NaN not clamped to NULL"

    # -- run compaction --
    vacuum_table(pg_conn, table_name)

    # -- post-compaction checks --
    cnt = run_query(f"SELECT count(*) FROM {table_name};", pg_conn)[0][0]
    assert cnt == total_rows, f"post-vacuum row count: {cnt}"

    if clamped_hi is not None:
        post = run_query(
            f"SELECT count(*) FROM {table_name} WHERE {col_name} = '{clamped_hi}';",
            pg_conn,
        )[0][0]
        assert post == 1, f"post-vacuum: clamped value not found via {clamped_hi}"
    else:
        post = run_query(
            f"SELECT count(*) FROM {table_name} WHERE {col_name} IS NULL;",
            pg_conn,
        )[0][0]
        assert post == 1, "post-vacuum: NaN NULL lost after compaction"

    for v in normal_vals:
        r = run_query(
            f"SELECT count(*) FROM {table_name} WHERE {col_name} = '{v}';",
            pg_conn,
        )[0][0]
        assert r == 1, f"post-vacuum: normal value {v} not found"

    run_command("RESET TIME ZONE;", pg_conn)
    run_command(f"DROP TABLE {table_name} CASCADE", pg_conn)
    pg_conn.commit()


def test_target_row_group_size(pg_conn, duckdb_conn, extension, tmp_path):
    parquet_path = tmp_path / "test.parquet"

    run_command("create table test_target_row_group_size(a text);", pg_conn)

    run_command(
        """
                INSERT INTO test_target_row_group_size SELECT ( 
                    WITH chars AS ( SELECT substr('abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789', floor(random()*62 + 1)::int, 1) AS ch
                                     FROM generate_series(1, 20000))
                    SELECT string_agg(ch, '') AS random_text FROM chars
                ) from generate_series(1, 20000) i;                
                """,
        pg_conn,
    )

    copy_command = (
        f"COPY test_target_row_group_size TO '{parquet_path}' WITH (format 'parquet');"
    )

    # copy with row_group_size_bytes disabled
    run_command("SET pg_lake_table.target_row_group_size_mb to 0;", pg_conn)

    run_command(copy_command, pg_conn)

    duckdb_conn.execute(
        "SELECT count(*) FROM parquet_metadata($1)", [str(parquet_path)]
    )
    row_groups_default = duckdb_conn.fetchall()[0][0]

    # default row_group_size = ~122k rows so we expect 1 row group for 20k rows by default
    assert row_groups_default == 1

    # copy with row_group_size_bytes = 2MB
    run_command("SET pg_lake_table.target_row_group_size_mb to '2MB';", pg_conn)

    run_command(copy_command, pg_conn)

    duckdb_conn.execute(
        "SELECT count(*) FROM parquet_metadata($1)", [str(parquet_path)]
    )
    row_groups_2mb = duckdb_conn.fetchall()[0][0]

    # copy with row_group_size_bytes = 128MB
    run_command("SET pg_lake_table.target_row_group_size_mb to '128MB';", pg_conn)

    run_command(copy_command, pg_conn)

    duckdb_conn.execute(
        "SELECT count(*) FROM parquet_metadata($1)", [str(parquet_path)]
    )
    row_groups_128mb = duckdb_conn.fetchall()[0][0]

    assert row_groups_2mb > row_groups_128mb > row_groups_default

    pg_conn.rollback()


